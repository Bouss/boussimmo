<?php

namespace App\Model;

class GmailLabel
{
    /**
     * @var string
     */
    private $id;

    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $type;

    /**
     * @return string
     */
    public function getId(): string
    {
        return $this->id;
    }

    /**
     * @param string $id
     *
     * @return GmailLabel
     */
    public function setId(string $id): GmailLabel
    {
        $this->id = $id;

        return $this;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     *
     * @return GmailLabel
     */
    public function setName(string $name): GmailLabel
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return string
     */
    public function getType(): string
    {
        return $this->type;
    }

    /**
     * @param string $type
     *
     * @return GmailLabel
     */
    public function setType(string $type): GmailLabel
    {
        $this->type = $type;

        return $this;
    }
}
